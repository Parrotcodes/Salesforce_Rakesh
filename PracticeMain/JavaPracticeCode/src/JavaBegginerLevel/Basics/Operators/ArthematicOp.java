package JavaBegginerLevel.Basics.Operators;

public class ArthematicOp {
    public static void main(String[] args) {
int n1 = 10;
int n2 = 23;

        System.out.println(n1+n2);
        System.out.println(n1-n2);
        System.out.println(n1*n2);
        System.out.println(n1/n2);
        System.out.println(n1%n2);

        n1 += 5;
        n2++; //incr
        ++n1; //pre-incr
    }
}
