package AdvConcepts.keywords;

public class AccessModifiers {
    public static void main(String[] args) {
//        public, private, protected, default
//        Access - public (anywhere) ---- same class, same package subclass, diff package subclass, diff package non-subclass
//               - private(same class) ---- same class
//               - default(same package) ---- same class, same package subclass, same package non-subclass
//               - protected(same package + subclass) ---- same class, same package subclass, same package non-subclass, diff package subclass
//                        Note(protected): -> outside package, but it should be subclassed (which extends the outside parent class)
    }
}
